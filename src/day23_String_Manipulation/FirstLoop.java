package day23_String_Manipulation;

public class FirstLoop {
    public static void main(String[] args) {
        int i = 1;
        while(i <=3){
            System.out.println(i);
           ++i;
        }
        int apples = 0;
        while (apples <=3){
            System.out.println("apple-> "+apples);
            apples++;
        }
        System.out.println("apple-> "+apples);

    }

}
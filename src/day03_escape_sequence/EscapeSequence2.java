package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[]args){
        System.out.println("\"Iphone case\"");
        System.out.println("\"iphone\",");
        System.out.println("\"iphone\", \"iphone\", \"xbox\", \"webcam\",\"monitor\"");
        System.out.println("\"iphone\", \"iphone\", \"xbox\", \n \"webcam\",\"monitor\"");
        System.out.println("    ^     ");
        System.out.println("  /   \\ ");
        System.out.println("  |   |    ");
        System.out.println("  -----    ");




    }
}
